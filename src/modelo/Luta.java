package modelo;

public class Luta {
    private int desafiado;
    private int desafiante;
    private int rounds;
    private Boolean aprovado;

    public int getDesafiado() {
        return desafiado;
    }

    public void setDesafiado(int desafiado) {
        this.desafiado = desafiado;
    }

    public int getDesafiante() {
        return desafiante;
    }

    public void setDesafiante(int desafiante) {
        this.desafiante = desafiante;
    }

    public int getRounds() {
        return rounds;
    }

    public void setRounds(int rounds) {
        this.rounds = rounds;
    }

    public Boolean getAprovado() {
        return aprovado;
    }

    public void setAprovado(Boolean aprovado) {
        this.aprovado = aprovado;
    }
    
}
